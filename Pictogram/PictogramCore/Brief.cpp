float Brief::SmoothePixel(int x, int y, int lv, cv::Mat& integralBoxImage)
{    
    int w = param.kernelSize[lv]/2;
    int ymin = y-w-1;
    int xmin = x-w-1;
    
    //        if(ymin<-1 || xmin<-1 || x+w>=integralBoxImage[0].length || y+w>=integralBoxImage.length)
    //        {
    //            System.out.println("["+x+", "+y+"] "+ lv);
    //            return 0;
    //        }
    
    float p = integralBoxImage.at<int>(y+w,x+w);
    if(xmin>=0)
        p -= integralBoxImage.at<int>(y+w,xmin);
    if(ymin>=0)
        p -= integralBoxImage.at<int>(ymin,x+w);
    if(xmin>=0 && ymin>=0)
        p += integralBoxImage.at<int>(ymin,xmin);
    //        integralBoxImage[x+w][y+w]+integralBoxImage[x-w-1][y-w-1]
    //                -integralBoxImage[x+w][y-w-1]-integralBoxImage[x-w-1][y+w];
    return p / (param.kernelSize[lv]*param.kernelSize[lv]);
}

int Brief::ComparePixels(int x1, int y1, int x2, int y2, int lv, cv::Mat& integralBoxImage)
{
    float p1 = SmoothePixel(x1,y1,lv,integralBoxImage);
    float p2 = SmoothePixel(x2,y2,lv,integralBoxImage);
    if(p1 < p2)
        return 1;
    return 0;
}

void Brief::GetBriefDescriptor(BriefDescriptor& desc, Keypoint& kp, cv::Mat& integralBoxImage)
{
    for(int i=0;i<param.nPairs; i++)
    {
        int b = ComparePixels(  (int)(samplePointsX[i][0]*param.kpSize[kp.lv]/5+kp.x),
                              (int)(samplePointsX[i][1]*param.kpSize[kp.lv]/5+kp.y),
                              (int)(samplePointsY[i][0]*param.kpSize[kp.lv]/5+kp.x),
                              (int)(samplePointsY[i][1]*param.kpSize[kp.lv]/5+kp.y),
                              kp.lv, integralBoxImage);
        desc.SetBit(b, i);
    }
}

